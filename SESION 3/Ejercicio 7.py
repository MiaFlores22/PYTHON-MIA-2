""" Lista (reverse): Invierte el orden de los elementos  de una lista """

datos_persona = ["Susana", "Gutierrez", 38, "20/04/1986"]
print("Los datos de la lista INICIAL son: {}".format(datos_persona))

datos_persona.reverse()
print("Los datos de la lista ACTUALIZADA son: {}".format(datos_persona))
