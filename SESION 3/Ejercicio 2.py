""" Listas en Python """

""" Lista pop(): Quitar un elementos de una posicion dada """

print()
lista = ["Peru", "España" ,"Brasil", "Argentina"]
print("Mi lista INICIAL es la siguiente: {}".format(lista))

lista.pop()
print("Mi lista ACTUALIZADA es la siguiente: {}".format(lista))

"ESTA ELIMINANDO EL VALOR DEL PAIS ESPAÑA"
lista.pop(1)
print("Mi lista ACTUALIZADA 2 es la siguiente: {}".format(lista))